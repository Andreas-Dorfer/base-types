namespace AD.BaseTypes;

/// <summary>
/// The generated cast operator.
/// </summary>
/// <remarks>
/// The default value is <see cref="Explicit"/>.
/// </remarks>
public enum Cast
{
    /// <summary>
    /// An explicit cast operator.
    /// </summary>
    Explicit,
    /// <summary>
    /// An implicit cast operator.
    /// </summary>
    Implicit,
    /// <summary>
    /// No cast operator.
    /// </summary>
    None
}
